# Aula 007 - Operadores Aritméticos
n1 = eval(input('Um valor: '))
n2 = eval(input('Outro valor: '))
soma = n1 + n2
subtracao = n1 - n2
multiplicacao = n1 * n2
divisao = n1 / n2
exponenciacao = n1 ** n2
print(f'Soma: {soma} \nSubtração: {subtracao} \nMultiplicação: {multiplicacao} \nDivisão: {divisao:.3f} \nExponenciação: {exponenciacao}')